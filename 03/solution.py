from functools import reduce


def count_trees_encountered_for_slope(grid, slope):
    grid_width = len(grid[0])
    x = 0
    y = 0

    trees_encountered = 0
    while y < len(grid) - 1:
        x = (x + slope[0]) % grid_width
        y += slope[1]
        trees_encountered += 1 if grid[y][x] == "#" else 0
    return trees_encountered


def solve(puzzle_input):
    grid = [[x for x in line] for line in puzzle_input.strip().split("\n")]

    # part 1
    print(count_trees_encountered_for_slope(grid, (3, 1)))

    # part 2
    slope_results = [
        count_trees_encountered_for_slope(grid, (1, 1)),
        count_trees_encountered_for_slope(grid, (3, 1)),
        count_trees_encountered_for_slope(grid, (5, 1)),
        count_trees_encountered_for_slope(grid, (7, 1)),
        count_trees_encountered_for_slope(grid, (1, 2))
    ]
    result = reduce((lambda x, y: x * y), slope_results)
    print(result)

    return


